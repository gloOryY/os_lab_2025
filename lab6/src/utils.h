#ifndef UTILS_H
#define UTILS_H

#include <stdint.h>

/*
 * MultModulo - безопасное умножение по модулю
 * 
 * Функция выполняет умножение двух чисел по модулю без переполнения.
 * Использует алгоритм двоичного умножения (binary multiplication).
 * 
 * Параметры:
 *   a   - первый множитель
 *   b   - второй множитель
 *   mod - модуль
 * 
 * Возвращает:
 *   Результат (a * b) % mod
 * 
 * Алгоритм:
 *   Представляем b в двоичном виде и последовательно удваиваем a,
 *   добавляя результат к итогу, когда текущий бит b равен 1.
 *   Все операции выполняются по модулю, чтобы избежать переполнения.
 */
uint64_t MultModulo(uint64_t a, uint64_t b, uint64_t mod);

#endif // UTILS_H
